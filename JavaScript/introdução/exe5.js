let num = 10
console.log(typeof num) // Exibe o tipo da variável num
num = String(num) // Converte num para string
console.log(typeof (num)) // Exibe o tipo da variável num após a conversão
num = Boolean(num) // Converte num para boolean
console.log(typeof (num)) // Exibe o tipo da variável num após a conversão
console.log(num) // Exibe o valor da variável num após a conversão